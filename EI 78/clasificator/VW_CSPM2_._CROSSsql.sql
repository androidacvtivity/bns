SELECT 
L.ITEM_CODE,
L.ITEM_PARENT,
L.NAME,
L.SHOW_ORDER

FROM
(
SELECT 
ITEM_CODE,
ITEM_PARENT,
NAME,
SHOW_ORDER

FROM CIS2.CLS_CLASS_ITEM

WHERE 
CLASS_CODE = 'CSPM2'

  AND ITEM_PARENT NOT IN  ('00.00.00')


ORDER BY 
SHOW_ORDER 
 ) L  LEFT JOIN (
 
 SELECT 
ITEM_CODE,
ITEM_PARENT,
NAME,
SHOW_ORDER
        
        FROM CIS2.VW_CLS_CLASS_ITEM
        
      WHERE 
CLASS_CODE = 'CSPM2'

  AND ITEM_PARENT NOT IN  ('00.00.00')


ORDER BY 
SHOW_ORDER 
 )  R ON R.ITEM_CODE = L.ITEM_CODE       
 
 WHERE 
 R.ITEM_CODE IS NOT NULL  