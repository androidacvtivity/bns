
SELECT
DISTINCT D.CUIIO,
COUNT(CASE WHEN D.FORM = 46 AND  D.PERIOADA IN (2008) AND CAPITOL IN (1) THEN 1 END) AS E_2019,
COUNT(CASE WHEN D.FORM = 46 AND  D.PERIOADA IN (2008) AND CAPITOL IN (404) THEN 1 END) AS I_2019,

COUNT(CASE WHEN D.FORM = 46 AND  D.PERIOADA IN (2009) AND CAPITOL IN (1) THEN 1 END) AS E_2020,
COUNT(CASE WHEN D.FORM = 46 AND  D.PERIOADA IN (2009) AND CAPITOL IN (404) THEN 1 END) AS I_2020,

COUNT(CASE WHEN D.FORM = 44 AND  D.PERIOADA IN (1048) AND CAPITOL IN (1) THEN 1 END) AS E_TRIM_1,
COUNT(CASE WHEN D.FORM = 44 AND  D.PERIOADA IN (1049) AND CAPITOL IN (1) THEN 1 END) AS E_TRIM_2,
COUNT(CASE WHEN D.FORM = 44 AND  D.PERIOADA IN (1050) AND CAPITOL IN (1) THEN 1 END) AS E_TRIM_3,
COUNT(CASE WHEN D.FORM = 44 AND  D.PERIOADA IN (1048) AND CAPITOL IN (14) THEN 1 END) AS I_TRIM_1,
COUNT(CASE WHEN D.FORM = 44 AND  D.PERIOADA IN (1049) AND CAPITOL IN (14) THEN 1 END) AS I_TRIM_2,
COUNT(CASE WHEN D.FORM = 44 AND  D.PERIOADA IN (1050) AND CAPITOL IN (14) THEN 1 END) AS I_TRIM_3
FROM
CIS2.VW_DATA_ALL D
WHERE
(D.PERIOADA IN (1048,1049,1050,2008,2009)
AND (D.FORM IN (44,46))
AND D.CAPITOL IN (14,1,404)
AND D.RIND IN('05','02','03')
AND NVAL(D.COL1) = 1)

GROUP BY
D.CUIIO