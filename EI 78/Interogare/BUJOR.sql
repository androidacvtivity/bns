SELECT
DISTINCT D.CUIIO,
COUNT(CASE WHEN D.PERIOADA IN (1040) AND CAPITOL IN (1) THEN 1 END) AS E_1040,
COUNT(CASE WHEN D.PERIOADA IN (1041) AND CAPITOL IN (1) THEN 1 END) AS E_1041,
COUNT(CASE WHEN D.PERIOADA IN (1042) AND CAPITOL IN (1) THEN 1 END) AS E_1042,
COUNT(CASE WHEN D.PERIOADA IN (1040) AND CAPITOL IN (14) THEN 1 END) AS I_1040,
COUNT(CASE WHEN D.PERIOADA IN (1041) AND CAPITOL IN (14) THEN 1 END) AS I_1041,
COUNT(CASE WHEN D.PERIOADA IN (1042) AND CAPITOL IN (14) THEN 1 END) AS I_1042
FROM
CIS2.VW_DATA_ALL D
WHERE
(D.PERIOADA IN (1040,1041,1042)
AND D.FORM = 44
AND D.CAPITOL IN (14,1)
AND D.RIND = '05'
AND D.COL1 = 1)
GROUP BY
D.CUIIO