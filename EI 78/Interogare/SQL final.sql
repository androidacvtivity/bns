--)

GROUP BY
D.CUIIO

)


WHERE 

E_2017 + I_2017 + E_2018 + I_2018 + E_TRIM_1 + E_TRIM_2 + E_TRIM_3 + I_TRIM_1 + I_TRIM_2 + I_TRIM_3 = 10
       
       )
       