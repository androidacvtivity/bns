





       SELECT 
       CUIIO,
       DENUMIRE,
       CUATM,
       CAEM2 

       FROM USER_BANCU.VW_KATALOG_EI_78_1043
       
       
       WHERE 
       
       CUIIO IN (
       SELECT

CUIIO

FROM 
 
(
SELECT
DISTINCT D.CUIIO,
COUNT(CASE WHEN D.FORM = 46 AND  D.PERIOADA IN (2006) AND CAPITOL IN (1) THEN 1 END) AS E_2017,
COUNT(CASE WHEN D.FORM = 46 AND  D.PERIOADA IN (2006) AND CAPITOL IN (404) THEN 1 END) AS I_2017,

COUNT(CASE WHEN D.FORM = 46 AND  D.PERIOADA IN (2007) AND CAPITOL IN (1) THEN 1 END) AS E_2018,
COUNT(CASE WHEN D.FORM = 46 AND  D.PERIOADA IN (2007) AND CAPITOL IN (404) THEN 1 END) AS I_2018,

COUNT(CASE WHEN D.FORM = 44 AND  D.PERIOADA IN (1040) AND CAPITOL IN (1) THEN 1 END) AS E_TRIM_1,
COUNT(CASE WHEN D.FORM = 44 AND  D.PERIOADA IN (1041) AND CAPITOL IN (1) THEN 1 END) AS E_TRIM_2,
COUNT(CASE WHEN D.FORM = 44 AND  D.PERIOADA IN (1042) AND CAPITOL IN (1) THEN 1 END) AS E_TRIM_3,
COUNT(CASE WHEN D.FORM = 44 AND  D.PERIOADA IN (1040) AND CAPITOL IN (14) THEN 1 END) AS I_TRIM_1,
COUNT(CASE WHEN D.FORM = 44 AND  D.PERIOADA IN (1041) AND CAPITOL IN (14) THEN 1 END) AS I_TRIM_2,
COUNT(CASE WHEN D.FORM = 44 AND  D.PERIOADA IN (1042) AND CAPITOL IN (14) THEN 1 END) AS I_TRIM_3
FROM
CIS2.VW_DATA_ALL D
WHERE
(D.PERIOADA IN (1040,1041,1042,2006,2007)
AND (D.FORM IN (44,46))
AND D.CAPITOL IN (14,1,404)
AND D.RIND = '05'
AND D.COL1 = 1)


--
