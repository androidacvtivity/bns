SELECT 
L.CONTROL,
L.CUIIO,
L.REZULTAT

FROM

(
SELECT 
L.CONTROL,
L.CUIIO,
L.REZULTAT

FROM CIS2.CONTROL L 

WHERE 
L.FORM = 44 AND L.PERIOADA = 1046
 AND L.REZULTAT <> 'OK'
 
 ) L LEFT JOIN (
    
 SELECT 
L.CONTROL,
L.CUIIO,
L.REZULTAT 
 
 FROM USER_BANCU.CONTROL_44_1046 L
 ) R ON R.CONTROL = L.CONTROL AND R.CUIIO = L.CUIIO  AND TRIM(L.REZULTAT) = TRIM(R.REZULTAT)
 
 
 WHERE 
 
 R.CONTROL IS NOT  NULL 