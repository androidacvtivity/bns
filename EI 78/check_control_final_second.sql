SELECT 
L.CONTROL,
L.CUIIO,
R.CUATM, 
--L.CUIIO_VERS,
L.REZULTAT

FROM 

(
SELECT 
L.CONTROL,
L.CUIIO,
L.CUIIO_VERS,
L.REZULTAT

FROM CIS2.CONTROL L 

WHERE 
L.FORM = 44 AND L.PERIOADA = 1046
 AND L.REZULTAT <> 'OK'
 ) L  INNER JOIN CIS2.RENIM R ON L.CUIIO = R.CUIIO  AND L.CUIIO_VERS = R.CUIIO_VERS 
 
 ORDER BY 
 R.CUATM