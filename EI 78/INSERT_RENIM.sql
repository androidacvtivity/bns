  TO_NUMBER(TRIM(R.CUIIO)) CUIIO,
  TO_NUMBER(TRIM(R.CUIIO_VERS)) CUIIO_VERS,
  TRIM(R.DENUMIRE)  DENUMIRE,
  1 EDIT_USER,
  '1' STATUT,
  TRIM(R.CUATM) CUATM,           
  R.CFP,
  NULL CFOJ,
