(

     SELECT
       L.CUIIO,
       L.CUIIO_VERS,
       L.DENUMIREA DENUMIRE,
       L.CUATM,
       L.CAEM2,
       L.CFP

      FROM USER_BANCU.EI_78_1036 L 
               
