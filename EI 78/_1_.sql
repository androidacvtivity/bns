(


SELECT  
    2 ORDINE,
    R.CUIIO,
    R.CUATM,
    R.PACHET,

    R.SERV_CODUL,
    R.SERV_CODUL_OLD,
    R.DENUMIRE,
    SUM(L.COL1) AS  COL1,
    SUM(R.COL2) AS  COL2, 

    
      NVAL(SUM(L.COL1)) - NVAL(SUM(R.COL2))  AS COL3 
