 D.CUATM,
 C.FULL_CODE,
 R.DENUMIRE,
  D.PACHET,
 D.CUIIO,
CI.A01,
 CI.ITEM_CODE,
 CI.NAME
  
  

  ORDER BY 
 C.FULL_CODE,
  D.CUIIO
  
  
  ) R ON R.CUIIO = L.CUIIO AND L.SERV_CODUL = R.SERV_CODUL
  


GROUP BY 

   R.CUIIO,
    R.CUATM,
    R.PACHET,

    R.SERV_CODUL,
    R.SERV_CODUL_OLD,
    R.DENUMIRE
    
    
    HAVING 
    
    NVAL(SUM(L.COL1)) - NVAL(SUM(R.COL2)) >   500000
    
    OR
    
     NVAL(SUM(R.COL2)) - NVAL(SUM(L.COL1)) >   500000
    
    
    ORDER BY

    R.CUIIO,
    R.SERV_CODUL
    )
    


 ORDER BY

    CUIIO,
    SERV_CODUL
