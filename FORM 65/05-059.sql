SELECT DISTINCT 
nval(SUM(CASE WHEN D.RIND IN ('001') THEN D.COL1 ELSE 0 END))  

||'<>'|| 
  TO_NUMBER(SUM(CASE WHEN D.RIND IN ('051','031','112','398','417','417','643','762','795','804','860') THEN D.COL1 ELSE 0 END)  + 
  SUM(CASE WHEN D.RIND NOT  IN ('051','031','112','398','417','417','643','762','795','804','860','-', '999','001','002') THEN D.COL1 ELSE 0 END))

 
  
  AS REZULTAT
FROM
  VW_DATA_ALL D 
WHERE
  (D.PERIOADA=:PERIOADA         ) AND
  (D.CUIIO=:CUIIO               ) AND
  (D.CUIIO_VERS=:CUIIO_VERS     OR :CUIIO_VERS = -1) AND
  (D.FORM = :FORM               OR :FORM = -1) AND
  (D.FORM_VERS=:FORM_VERS       OR :FORM_VERS = -1) AND
  (D.CAPITOL=:CAPITOL           OR :CAPITOL = -1) AND
  (D.CAPITOL_VERS=:CAPITOL_VERS OR :CAPITOL_VERS = -1) AND
  (D.ID_MD=:ID_MD               OR :ID_MD = -1) AND
  
  D.FORM IN (5)  AND
  D.CAPITOL IN (312)

HAVING
  
SUM(CASE WHEN D.RIND IN ('001') THEN NVAL(D.COL1) ELSE 0 END) <> 

 SUM(CASE WHEN D.RIND NOT  IN ('001','002','999') THEN NVAL(D.COL1) ELSE 0 END)
