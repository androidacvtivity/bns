SELECT 
     CTM.NR_ROW   AS NR_SECTIE,
     CTM.NUME_ROW AS NUME_SECTIE,
    R.RIND AS NR_ROW, 
    R.ORDINE ORDINE, 
    R.DENUMIRE NUME_ROW, 
    CIS2.NVAL(SUM(CASE WHEN  D.UNIT_CODE = CR.UNIT_CODE AND CC.FULL_CODE LIKE '%'||CTM.CS_CUATM||'%' AND CR.TIP = '0'   THEN  CIS2.NVAL(D.COL1)  ELSE NULL END)) AS COL1,
    CIS2.NVAL(SUM(CASE WHEN  D.UNIT_CODE = CR.UNIT_CODE AND CC.FULL_CODE LIKE '%'||CTM.CS_CUATM||'%' AND CR.TIP = 'TIP2'   THEN  CIS2.NVAL(D.COL1)  ELSE NULL END)) AS COL2,
    CIS2.NVAL(SUM(CASE WHEN  D.UNIT_CODE = CR.UNIT_CODE AND CC.FULL_CODE LIKE '%'||CTM.CS_CUATM||'%' AND CR.TIP = 'TIP3'   THEN  CIS2.NVAL(D.COL1)  ELSE NULL END)) AS COL3



FROM  CIS2.VW_DATA_ALL_GC D  
  INNER JOIN CIS2.MD_RIND R ON D.RIND=R.RIND AND D.ID_MD = R.ID_MD
  INNER JOIN CIS2.VW_CL_CUATM CC ON(D.CUATM = CC.CODUL)
   CROSS JOIN 
  (
    SELECT
      DECODE(CS.CODUL,      
      '0000000','01','0100000','02','1111111','03','0300000','04',
      '1400000','05','3400000','06','3600000','07','4100000','08','4300000','09',
      '4500000','10','4800000','11','6200000','12','7100000','13','7400000','14',
      '7800000','15','2222222','16','1000000','17','2500000','18','3100000','19',
      '3800000','20','5300000','21','5500000','22','6000000','23','6400000','24',
      '6700000','25','8000000','26','8300000','27','8900000','28','9200000','29',
      '3333333','30','1200000','31','1700000','32','2100000','33','2700000','34',
      '2900000','35','5700000','36','8500000','37','8700000','38','9600000','39',
      '9601000','40')
      AS NR_ROW,
      CS.DENUMIRE AS NUME_ROW,
      CS.CODUL AS CS_CUATM
    FROM
      CIS2.VW_CL_CUATM CS
    WHERE
      CS.CODUL 
      IN('0000000','0100000','1111111','0300000','1400000','3400000','3600000','4100000','4300000','4500000',
         '4800000','6200000','7100000','7400000','7800000','2222222','1000000','2500000','3100000','3800000',
         '5300000','5500000','6000000','6400000','6700000','8000000','8300000','8900000','9200000','3333333',
         '1200000','1700000','2100000','2700000','2900000','5700000','8500000','8700000','9600000','9601000')
  ) CTM 
  
  
  CROSS JOIN (
  
      SELECT 
     DISTINCT D.UNIT_CODE, 
    'TIP2' TIP 

    FROM CIS2.VW_DATA_ALL_GC 
    
    D WHERE 
    
   --(D.PERIOADA BETWEEN FLOOR(:pPERIOADA/4)*4 AND :pPERIOADA)  AND 
   (D.PERIOADA IN (:pPERIOADA))  AND    
    D.FORM_VERS = :pFORM_VERS  AND D.FORM = :pFORM   
    AND 
    D.CUATM_FULL LIKE '%'||:pCOD_CUATM||';%' 
    AND D.FORM IN (62) 
    AND D.CAPITOL IN (1118) 
    AND D.RIND IN ('TIP2') 
    
    
    UNION ALL 
    
     SELECT 
     DISTINCT D.UNIT_CODE, 
    'TIP3' TIP 

    FROM CIS2.VW_DATA_ALL_GC 
    
    D WHERE 
       --(D.PERIOADA BETWEEN FLOOR(:pPERIOADA/4)*4 AND :pPERIOADA)  AND 
   (D.PERIOADA IN (:pPERIOADA))  AND  
    
    D.FORM_VERS = :pFORM_VERS  AND D.FORM = :pFORM   
    AND 
    D.CUATM_FULL LIKE '%'||:pCOD_CUATM||';%' 
    AND D.FORM IN (62) 
    AND D.CAPITOL IN (1118) 
    AND D.RIND IN ('TIP3') 
    
    
     UNION ALL 
    
     SELECT 
     DISTINCT D.UNIT_CODE, 
    '0' TIP 

    FROM CIS2.VW_DATA_ALL_GC 
    
    D WHERE 
    
       --(D.PERIOADA BETWEEN FLOOR(:pPERIOADA/4)*4 AND :pPERIOADA)  AND 
   (D.PERIOADA IN (:pPERIOADA))  AND   
     
    D.FORM_VERS = :pFORM_VERS  AND D.FORM = :pFORM   
    AND 
    D.CUATM_FULL LIKE '%'||:pCOD_CUATM||';%' 
    AND D.FORM IN (62) 
    AND D.CAPITOL IN (1118) 
    AND D.RIND IN ('0') 

  ) CR

  
  
  WHERE 
  
     --(D.PERIOADA BETWEEN FLOOR(:pPERIOADA/4)*4 AND :pPERIOADA)  AND 
   (D.PERIOADA IN (:pPERIOADA))  AND   
  (D.FORM =:pFORM) AND
  (D.FORM_VERS =:pFORM_VERS) AND 
  (:pID_MDTABLE =:pID_MDTABLE) AND 
  (D.CUATM_FULL LIKE '%'||:pCOD_CUATM||';%') AND
  D.FORM IN (62)  AND
  D.CAPITOL IN (1115) 
  
  
  GROUP BY 
    R.RIND, 
    R.ORDINE, 
    R.DENUMIRE,
  CTM.NR_ROW,   
  CTM.NUME_ROW 