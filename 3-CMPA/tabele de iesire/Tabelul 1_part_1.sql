SELECT 
DISTINCT D.UNIT_CODE 
FROM CIS2.VW_DATA_ALL_GC D 
WHERE 
D.FORM_VERS = :pFORM_VERS  
AND D.FORM = :pFORM   AND D.PERIOADA IN  (1045,1046,1047) 
AND 
    D.CUATM_FULL LIKE '%'||:pCOD_CUATM||';%' AND D.FORM IN (62) AND D.CAPITOL IN (1118) AND D.RIND IN ('TIP2') 
    
    GROUP BY D.UNIT_CODE  HAVING
    
    CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN (1118) AND D.RIND IN ('TIP2') THEN  1 ELSE NULL END))>0
    --count  Perioada (1045) -- 609
    --count  Perioada (1044, 1045, 1046,1047) -- 609