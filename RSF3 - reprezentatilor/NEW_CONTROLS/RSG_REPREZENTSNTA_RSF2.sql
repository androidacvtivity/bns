SELECT  
L.CUIIO 

FROM 

 (
 SELECT 
 DISTINCT
 
  D.CUIIO,
  D.FORM
FROM CIS2.VW_DATA_ALL_FR D
WHERE
  (D.PERIOADA IN (:PERIOADA)) AND
--  (D.CUIIO=:CUIIO OR :CUIIO = -1) AND
   D.FORM  IN(66)

GROUP BY 
  D.FORM,
  D.CUIIO
  
  ORDER BY
   
  D.CUIIO,
  D.FORM
   
  ) L  LEFT JOIN (
  
  SELECT 
 DISTINCT
 
  D.CUIIO,
  D.FORM
FROM CIS2.VW_DATA_ALL_FR D
WHERE
  (D.PERIOADA IN (:PERIOADA)) AND
--  (D.CUIIO=:CUIIO OR :CUIIO = -1) AND
   D.FORM  IN(59)

GROUP BY 
  D.FORM,
  D.CUIIO
  
  ORDER BY
   
  D.CUIIO,
  D.FORM
  ) R ON L.CUIIO = R.CUIIO 
  
  WHERE 
  R.CUIIO  IS NOT  NULL