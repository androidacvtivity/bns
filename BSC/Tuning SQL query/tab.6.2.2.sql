SELECT
  D.CUIIO,
  R.CUATM,
  MR.CAPITOL,
  SUM(CASE WHEN MR.CAPITOL IN (1017) AND  MR.RIND IN ('000')  THEN CIS2.NVAL(D.COL2) ELSE NULL END) AS R000_2,
  SUM(CASE WHEN MR.CAPITOL IN (1017) AND  MR.RIND IN ('000')  THEN CIS2.NVAL(D.COL4) ELSE NULL END) AS R000_4,
  SUM(CASE WHEN MR.CAPITOL IN (1017) AND  MR.RIND IN ('000')  THEN CIS2.NVAL(D.COL6) ELSE NULL END) AS R000_6,
  SUM(CASE WHEN MR.CAPITOL IN (1018) AND  MR.RIND IN ('2002') THEN CIS2.NVAL(D.COL1) ELSE NULL END) AS R2002_1,
  SUM(CASE WHEN MR.CAPITOL IN (1018) AND  MR.RIND IN ('2002') THEN CIS2.NVAL(D.COL2) ELSE NULL END) AS R2002_2,
  SUM(CASE WHEN MR.CAPITOL IN (1018) AND  MR.RIND IN ('2002') THEN CIS2.NVAL(D.COL3) ELSE NULL END) AS R2002_3,
  SUM(CASE WHEN MR.CAPITOL IN (1018) AND  MR.RIND IN ('220')  THEN CIS2.NVAL(D.COL1) ELSE NULL END) AS R220_1,
  SUM(CASE WHEN MR.CAPITOL IN (1018) AND  MR.RIND IN ('220')  THEN CIS2.NVAL(D.COL2) ELSE NULL END) AS R220_2,
  SUM(CASE WHEN MR.CAPITOL IN (1018) AND  MR.RIND IN ('220')  THEN CIS2.NVAL(D.COL3) ELSE NULL END) AS R220_3, 
  SUM(CASE WHEN MR.CAPITOL IN (1018) AND  MR.RIND IN ('230')  THEN CIS2.NVAL(D.COL1) ELSE NULL END) AS R230_1,
  SUM(CASE WHEN MR.CAPITOL IN (1018) AND  MR.RIND IN ('230')  THEN CIS2.NVAL(D.COL2) ELSE NULL END) AS R230_2,
  SUM(CASE WHEN MR.CAPITOL IN (1018) AND  MR.RIND IN ('230')  THEN CIS2.NVAL(D.COL3) ELSE NULL END) AS R230_3, 
  SUM(CASE WHEN MR.CAPITOL IN (1018) AND  MR.RIND IN ('210')  THEN CIS2.NVAL(D.COL1) ELSE NULL END) AS R210_1,
  SUM(CASE WHEN MR.CAPITOL IN (1018) AND  MR.RIND IN ('210')  THEN CIS2.NVAL(D.COL2) ELSE NULL END) AS R210_2,
  SUM(CASE WHEN MR.CAPITOL IN (1018) AND  MR.RIND IN ('210')  THEN CIS2.NVAL(D.COL3) ELSE NULL END) AS R210_3, 
  SUM(CASE WHEN MR.CAPITOL IN (1018) AND  MR.RIND IN ('200')  THEN CIS2.NVAL(D.COL1) ELSE NULL END) AS R200_1,
  SUM(CASE WHEN MR.CAPITOL IN (1018) AND  MR.RIND IN ('200')  THEN CIS2.NVAL(D.COL2) ELSE NULL END) AS R200_2,
  SUM(CASE WHEN MR.CAPITOL IN (1018) AND  MR.RIND IN ('200')  THEN CIS2.NVAL(D.COL3) ELSE NULL END) AS R200_3
   
FROM
  CIS2.DATA_ALL D
         INNER JOIN CIS2.MD_RIND MR ON (MR.ID_MD = D.ID_MD)
         INNER JOIN CIS2.RENIM R ON R.CUIIO = D.CUIIO AND R.CUIIO_VERS = D.CUIIO_VERS 
         INNER JOIN CIS2.VW_CL_CUATM C ON C.CODUL = R.CUATM
     
WHERE
  --(D.PERIOADA =:pPERIOADA) AND
  (D.PERIOADA BETWEEN FLOOR(:pPERIOADA/4)*4 AND :pPERIOADA)  AND     
  (:pFORM =:pFORM) AND
  (:pFORM_VERS =:pFORM_VERS) AND 
  (:pID_MDTABLE =:pID_MDTABLE) AND
 (C.FULL_CODE LIKE '%'||:pCOD_CUATM||';%') AND
  D.FORM IN (19) AND
  MR.CAPITOL IN (1017,1018)
 -- AND D.CUIIO IN (5921029)
    
GROUP BY
  D.CUIIO,
  R.CUATM,
  MR.CAPITOL
  
  ORDER BY 
  D.CUIIO