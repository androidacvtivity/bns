



SELECT
P.NUM,
P.PERIOADA

 
FROM CIS2.MD_PERIOADA P
            INNER JOIN (


SELECT
P.ANUL,
P.PERIOADA,
P.NUM,
P.TIP_PERIOADA
 
FROM CIS2.MD_PERIOADA P

WHERE
P.PERIOADA = :pPERIOADA 


GROUP BY 
P.ANUL,
P.PERIOADA,
P.NUM, 
P.TIP_PERIOADA

) L ON  P.ANUL = L.ANUL  AND P.TIP_PERIOADA = 4

