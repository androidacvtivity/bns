SELECT CC.CODUL,
   SUM(A.COL1) AS COL1
   FROM ( SELECT  'A0111' CAEM2, 1 COL1
          FROM DUAL
          UNION ALL 
SELECT  'A0112' CAEM2,   1 COL1
          FROM DUAL UNION  ALL
          SELECT   NULL CAEM2, 1 COL1
          FROM DUAL   
            )  A INNER JOIN (SELECT  '00000'   CODUL,'00000'   GRUPA,
'00000;'  FULL_COD FROM DUAL
             UNION ALL  SELECT   'A0000'    CODUL, '00000'         GRUPA,
'00000;A0000;'  FULL_CODE

 FROM DUAL
 
 UNION ALL 
 
 SELECT  
'A0100'   CODUL,
'A0000'   GRUPA,
'00000;A0000;A0100;'  FULL_CODE

 FROM DUAL
 
 
 UNION ALL 
 
 SELECT  
'A0110'   CODUL,
'A0100'   GRUPA,
'00000;A0000;A0100;A0110;'  FULL_CODE

 FROM DUAL
 
 
 UNION ALL 
 
SELECT  
'A0111'   CODUL,
'A0110'   GRUPA,
'00000;A0000;A0100;A0110;A0111;'  FULL_CODE

 FROM DUAL
 
 UNION ALL 
 
SELECT  
'A0112'   CODUL,
'A0110'   GRUPA,
'00000;A0000;A0100;A0110;A0112;'  FULL_CODE

 FROM DUAL) C ON (C.CODUL = A.CAEM2) 
            
     INNER JOIN (
     
     SELECT  
'00000'   CODUL,
'00000'   GRUPA,
'00000;'  FULL_CODE

 FROM DUAL
 
 
 UNION ALL
 
 SELECT  
'A0000'         CODUL,
'00000'         GRUPA,
'00000;A0000;'  FULL_CODE

 FROM DUAL
 
 UNION ALL 
 
 SELECT  
'A0100'   CODUL,
'A0000'   GRUPA,
'00000;A0000;A0100;'  FULL_CODE

 FROM DUAL
 
 
 UNION ALL 
 
 SELECT  
'A0110'   CODUL,
'A0100'   GRUPA,
'00000;A0000;A0100;A0110;'  FULL_CODE

 FROM DUAL
 
 
 UNION ALL 
 
SELECT  
'A0111'   CODUL,
'A0110'   GRUPA,
'00000;A0000;A0100;A0110;A0111;'  FULL_CODE

 FROM DUAL
 
 UNION ALL 
 
SELECT  
'A0112'   CODUL,
'A0110'   GRUPA,
'00000;A0000;A0100;A0110;A0112;'  FULL_CODE

 FROM DUAL
     
     
     ) CC ON (C.FULL_CODE LIKE '%'||CC.CODUL||';%')
 
 
 GROUP BY 
 CC.CODUL,
 CC.FULL_CODE
 
 ORDER BY 
 CC.FULL_CODE
  
  