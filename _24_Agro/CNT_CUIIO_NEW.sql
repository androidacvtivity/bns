  DISTINCT D.CUIIO,
  MAX(RE.DENUMIRE) DENUMIRE,
  SUM(CASE WHEN D.PERIOADA IN (:pPERIOADA-1) AND D.RIND IN ('08') AND CC.FULL_CODE LIKE '%'||R.CS_CUATM||'%' AND CF.NR_ROW LIKE '%'|| D.CFOJ ||'%' AND CF.FORM LIKE '%'|| D.FORM ||'%' THEN D.COL1 END) AS COL1,
