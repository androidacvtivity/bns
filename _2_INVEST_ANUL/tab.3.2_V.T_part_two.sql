FROM (

SELECT
  
   R.RIND AS NR_ROW,
   R.ORDINE AS ORDINE,
   R.DENUMIRE AS NUME_ROW,
   
  SUM(DISTINCT CASE WHEN R.RIND = D.RIND  THEN D.COL2 ELSE 0 END)AS COL1,
  
   
  
