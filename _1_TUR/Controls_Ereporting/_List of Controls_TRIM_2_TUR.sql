
SELECT 

  CONTROL,
  CONTROL_VERS,
  PERIOADA,
  CUIIO,
  CUIIO_VERS,
  FORM,
  FORM_VERS,
  CAPITOL,
  CAPITOL_VERS,
  ID_MD,
  REZULTAT,
  DATA_REG      

FROM CIS2.CONTROL

WHERE 

FORM = 6  AND PERIOADA IN  (1050)
 AND REZULTAT <> 'OK'
 
 AND CONTROL IN ('06-026','06-027','06-027-1')
 
 
 ORDER BY 
 DATA_REG DESC