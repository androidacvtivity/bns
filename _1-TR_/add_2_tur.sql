INSERT INTO USER_BANCU.KAT_1_AUTO_FINAL

(

  CUIIO,
  CUIIO_VERS,
  DENUMIRE,
  CUATM,
  CFP,
  CAEM2
)



SELECT 

  D.CUIIO,
  D.CUIIO_VERS,
  D.DENUMIRE,
  D.CUATM,
  D.CFP,
  CA.CODUL CAEM2

FROM 
(

SELECT 

  D.CUIIO,
  D.CUIIO_VERS,
  D.DENUMIRE,
  D.CUATM,
  D.CFP,
  D.CAEM2

FROM 
(
SELECT 
  D.CUIIO,
  D.CUIIO_VERS,
  D.DENUMIRE,
 CASE WHEN (LENGTH(D.CUATM) = 6 AND (SUBSTR(D.CUATM,1,1) = 1  OR SUBSTR(D.CUATM,1,1) = 3 )) THEN '0'||D.CUATM ELSE CUATM END CUATM,
  D.CFP,
  D.CAEM2
    FROM USER_BANCU.KAT_1_AUTO D
) D   LEFT JOIN CIS2.VW_CL_CUATM C ON C.CODUL = D.CUATM

WHERE 
C.CODUL IS NOT NULL 

) D 

LEFT JOIN CIS2.VW_CL_CAEM2 CA ON CA.NUM_CODE = D.CAEM2   
WHERE
CA.NUM_CODE IS NOT  NULL  