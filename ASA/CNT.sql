



SELECT 
DISTINCT 
D.CUIIO,
R.DENUMIRE,
D.CUATM 


FROM CIS2.VW_DATA_ALL D

            INNER JOIN CIS2.RENIM R ON R.CUIIO = D.CUIIO AND 
                                    R.CUIIO_VERS = D.CUIIO_VERS

WHERE 
D.FORM = 64 
AND D.PERIOADA  = 2009
AND D.capitol=100 AND D.capitol_vers=2009
AND D.RIND IN ('1')

ORDER BY
D.CUATM 