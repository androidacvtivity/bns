



SELECT 
DISTINCT 
D.CUIIO,
--R.DENUMIRE,
D.CUATM,
R.CAEM2,
SUM (CASE WHEN capitol=1124 AND capitol_vers=2000  AND  D.RIND IN ('150') THEN D.COL1 ELSE NULL END) AS COL1,


SUM (CASE WHEN capitol=1126 AND capitol_vers=2000 AND D.RIND IN ('300')THEN D.COL1 ELSE NULL END) AS COL2,
SUM (CASE WHEN capitol=1126 AND capitol_vers=2000 AND D.RIND IN ('300')THEN D.COL2 ELSE NULL END) AS COL3, 

SUM (CASE WHEN capitol=1126 AND capitol_vers=2000 AND D.RIND IN ('310')THEN D.COL1 ELSE NULL END) AS COL4,
SUM (CASE WHEN capitol=1126 AND capitol_vers=2000 AND D.RIND IN ('310')THEN D.COL2 ELSE NULL END) AS COL5,

SUM (CASE WHEN capitol=1126 AND capitol_vers=2000 AND D.RIND IN ('320')THEN D.COL1 ELSE NULL END) AS COL6,
SUM (CASE WHEN capitol=1126 AND capitol_vers=2000 AND D.RIND IN ('320')THEN D.COL2 ELSE NULL END) AS COL7,  


SUM (CASE WHEN capitol=1126 AND capitol_vers=2000 AND D.RIND IN ('330')THEN D.COL1 ELSE NULL END) AS COL8,
SUM (CASE WHEN capitol=1126 AND capitol_vers=2000 AND D.RIND IN ('330')THEN D.COL2 ELSE NULL END) AS COL9,

 SUM (CASE WHEN capitol=1126 AND capitol_vers=2000 AND D.RIND IN ('340')THEN D.COL1 ELSE NULL END) AS COL10,
SUM (CASE WHEN capitol=1126 AND capitol_vers=2000 AND D.RIND IN ('340')THEN D.COL2 ELSE NULL END) AS COL11, 

 SUM (CASE WHEN capitol=1126 AND capitol_vers=2000 AND D.RIND IN ('350')THEN D.COL1 ELSE NULL END) AS COL12,
SUM (CASE WHEN capitol=1126 AND capitol_vers=2000 AND D.RIND IN ('350')THEN D.COL2 ELSE NULL END) AS COL13, 

 SUM (CASE WHEN capitol=1126 AND capitol_vers=2000 AND D.RIND IN ('360')THEN D.COL1 ELSE NULL END) AS COL15,
SUM (CASE WHEN capitol=1126 AND capitol_vers=2000 AND D.RIND IN ('360')THEN D.COL2 ELSE NULL END) AS COL16 



FROM CIS2.VW_DATA_ALL D

            INNER JOIN CIS2.RENIM R ON R.CUIIO = D.CUIIO AND 
                                    R.CUIIO_VERS = D.CUIIO_VERS

WHERE 
D.FORM = 64 
AND D.PERIOADA  = 2009

--AND capitol=1126 AND capitol_vers=2000 AND D.RIND IN ('310') 

GROUP BY 
D.CUIIO,
R.CAEM2,
R.DENUMIRE,
D.CUATM

ORDER BY
D.CUATM 