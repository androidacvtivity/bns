DECLARE -- ====================================================================

  CURSOR C IS
  SELECT
    :pCOD_CUATM AS pCOD_CUATM
  FROM
    DUAL;

BEGIN -- ======================================================================
  FOR CR IN C
  LOOP
   IF CR.pCOD_CUATM = '0000000' THEN
   
     INSERT INTO -- USER_BANCU.TABLE_OUT_TEST
   CIS2.TABLE_OUT
    (
      
  PERIOADA,
  FORM,
  FORM_VERS,
  ID_MDTABLE,
  COD_CUATM,
  NR_SECTIE,
  NUME_SECTIE,
  NR_SECTIE1,
  NUME_SECTIE1,
  NR_SECTIE2,
  NUME_SECTIE2,
  NR_ROW,
  ORDINE,
  DECIMAL_POS,
  NUME_ROW,  
  COL1, COL2, COL3, COL4, COL5, COL6, COL7, COL8, COL9, COL10, COL11
  
    )


SELECT

  :pPERIOADA AS PERIOADA,
  :pFORM AS FORM,
  :pFORM_VERS AS FORM_VERS,
  :pID_MDTABLE AS ID_MDTABLE,
  :pCOD_CUATM AS COD_CUATM,         
  '0' AS NR_SECTIE,
  '0' AS NUME_SECTIE,
  '0' AS NR_SECTIE1,
  '0' AS NUME_SECTIE1,
  '0' AS NR_SECTIE2,
  '0' AS NUME_SECTIE2,
  R.NR_ROW  AS NR_ROW,
  R.NR_ROW AS ORDINE,
 '00000000000' AS DECIMAL_POS, 
  R.NUME_ROW,

    SUM(CASE WHEN CC.FULL_CODE LIKE '%'||R.CS_CUATM||'%'  THEN CIS2.NVAL(D.COL1) + CIS2.NVAL(D.COL3) ELSE 0 END )  AS COL1,
    SUM(CASE WHEN CC.FULL_CODE LIKE '%'||R.CS_CUATM||'%'  THEN CIS2.NVAL(D.COL4) + CIS2.NVAL(D.COL2) ELSE 0 END )  AS COL2,
    
    
    
    ROUND(SUM(CASE WHEN CC.FULL_CODE LIKE '%'||R.CS_CUATM||'%'  THEN CIS2.NVAL(D.COL4) + CIS2.NVAL(D.COL2) END ) / CIS2.NOZERO( SUM(CASE WHEN CC.FULL_CODE LIKE '%'||R.CS_CUATM||'%'  THEN CIS2.NVAL(D.COL1) + CIS2.NVAL(D.COL3) ELSE 0 END )),2) AS COL3,
    
    
    SUM(CASE WHEN CC.FULL_CODE LIKE '%'||R.CS_CUATM||'%' THEN CIS2.NVAL(D.COL1) ELSE 0 END)  AS COL4,
    SUM(CASE WHEN CC.FULL_CODE LIKE '%'||R.CS_CUATM||'%' THEN CIS2.NVAL(D.COL2) ELSE 0 END )  AS COL5, 
    
    ROUND(SUM(CASE WHEN CC.FULL_CODE LIKE '%'||R.CS_CUATM||'%' THEN CIS2.NVAL(D.COL2) ELSE 0 END )  / CIS2.NOZERO( SUM(CASE WHEN CC.FULL_CODE LIKE '%'||R.CS_CUATM||'%' THEN CIS2.NVAL(D.COL1) ELSE 0 END ) ),2)  AS COL6,
    
     
    SUM(CASE WHEN CC.FULL_CODE LIKE '%'||R.CS_CUATM||'%'  THEN CIS2.NVAL(D.COL3) ELSE 0 END)  AS COL7,
    SUM(CASE WHEN CC.FULL_CODE LIKE '%'||R.CS_CUATM||'%'  THEN CIS2.NVAL(D.COL4) ELSE 0 END)  AS COL8, 
    
    ROUND(SUM(CASE WHEN CC.FULL_CODE LIKE '%'||R.CS_CUATM||'%'  THEN CIS2.NVAL(D.COL4) END)  / CIS2.NOZERO(  SUM(CASE WHEN CC.FULL_CODE LIKE '%'||R.CS_CUATM||'%'  THEN CIS2.NVAL(D.COL3) ELSE 0 END) ),2)  AS COL9, 
   
    SUM(CASE WHEN CC.FULL_CODE LIKE '%'||R.CS_CUATM||'%'  THEN CIS2.NVAL(D.COL5) END )  AS COL10, 
    SUM(CASE WHEN CC.FULL_CODE LIKE '%'||R.CS_CUATM||'%' THEN ROUND(CIS2.NVAL(D.COL6),1)  END )  AS COL11 
    
FROM   CIS2.VW_DATA_ALL D
INNER JOIN CIS2.VW_CL_CUATM CC ON (D.CUATM=CC.CODUL)
    CROSS JOIN 
  (
    SELECT
      DECODE(CS.CODUL, 
      '0000000','01',
      '0100000','02','1111111','03','0300000','04',
      '1400000','05','3400000','06','3600000','07','4100000','08','4300000','09',
      '4500000','10','4800000','11','6200000','12','7100000','13','7400000','14',
      '7800000','15','2222222','16','1000000','17','2500000','18','3100000','19',
      '3800000','20','5300000','21','5500000','22','6000000','23','6400000','24',
      '6700000','25','8000000','26','8300000','27','8900000','28','9200000','29',
      '3333333','30','1200000','31','1700000','32','2100000','33','2700000','34',
      '2900000','35','5700000','36','8500000','37','8700000','38','9600000','39',
      '9601000','40')
      AS NR_ROW,
      CS.DENUMIRE AS NUME_ROW,
      CS.CODUL AS CS_CUATM
    FROM
      CIS2.VW_CL_CUATM CS
    WHERE
      CS.CODUL 
      IN(
         '0000000',
         '0100000','1111111','0300000','1400000','3400000','3600000','4100000','4300000','4500000',
         '4800000','6200000','7100000','7400000','7800000','2222222','1000000','2500000','3100000','3800000',
         '5300000','5500000','6000000','6400000','6700000','8000000','8300000','8900000','9200000','3333333',
         '1200000','1700000','2100000','2700000','2900000','5700000','8500000','8700000','9600000','9601000')
  ) R 

         INNER JOIN CIS2.MD_RIND MR ON (MR.ID_MD = D.ID_MD) 
  WHERE 
  (D.PERIOADA =:pPERIOADA) AND
  (D.FORM =:pFORM) AND
  (D.FORM_VERS =:pFORM_VERS) AND 
  (:pID_MDTABLE =:pID_MDTABLE)  AND 
  (D.CUATM_FULL LIKE '%' ||:pCOD_CUATM||';%') AND
  D.FORM = 53 AND 
  D.CAPITOL IN (1074) 
  AND MR.RIND IN ('180')
  
  GROUP BY
  R.NR_ROW,
  R.NUME_ROW
 
-------------------------------------------------------------------------------------------------------
;


   ELSE
   
   
      INSERT INTO -- USER_BANCU.TABLE_OUT_TEST
       CIS2.TABLE_OUT
  (
      
  PERIOADA,
  FORM,
  FORM_VERS,
  ID_MDTABLE,
  COD_CUATM,
  NR_SECTIE,
  NUME_SECTIE,
  NR_SECTIE1,
  NUME_SECTIE1,
  NR_SECTIE2,
  NUME_SECTIE2,
  NR_ROW,
  ORDINE,
  DECIMAL_POS,
  NUME_ROW,  
  COL1,COL2, COL3, COL4, COL5, COL6, COL7, COL8, COL9, COL10, COL11
  )

SELECT
   :pPERIOADA AS PERIOADA,
  :pFORM AS FORM,
  :pFORM_VERS AS FORM_VERS,
  :pID_MDTABLE AS ID_MDTABLE,
  :pCOD_CUATM AS COD_CUATM,         
  '0' AS NR_SECTIE,
  '0' AS NUME_SECTIE,
  '0' AS NR_SECTIE1,
  '0' AS NUME_SECTIE1,
  '0' AS NR_SECTIE2,
  '0' AS NUME_SECTIE2,
ORDINE||'~'||ROWNUM  AS NR_ROW,
ORDINE,
'00000000000' AS DECIMAL_POS, 
NUME_ROW,

CASE WHEN COL1 = 0 THEN NULL ELSE COL1 END COL1,
CASE WHEN COL2 = 0 THEN NULL ELSE COL2 END COL2,
CASE WHEN COL3 = 0 THEN NULL ELSE COL3 END COL3,
CASE WHEN COL4 = 0 THEN NULL ELSE COL4 END COL4,
CASE WHEN COL5 = 0 THEN NULL ELSE COL5 END COL5,
CASE WHEN COL6 = 0 THEN NULL ELSE COL6 END COL6,
CASE WHEN COL7 = 0 THEN NULL ELSE COL7 END COL7,
CASE WHEN COL8 = 0 THEN NULL ELSE COL8 END COL8,
CASE WHEN COL9 = 0 THEN NULL ELSE COL9 END COL9,
CASE WHEN COL10 = 0 THEN NULL ELSE COL10 END COL10,
CASE WHEN COL11 = 0 THEN NULL ELSE COL11 END COL11



FROM
(
SELECT 
  RE.CUIIO AS ORDINE,
  RE.DENUMIRE AS NUME_ROW,
  SUM(CIS2.NVAL(D.COL1) + CIS2.NVAL(D.COL3))  AS COL1,
    SUM(CIS2.NVAL(D.COL4) + CIS2.NVAL(D.COL2) )  AS COL2,
    ROUND(SUM(CIS2.NVAL(D.COL4) + CIS2.NVAL(D.COL2)) / CIS2.NOZERO(SUM(CIS2.NVAL(D.COL1) + CIS2.NVAL(D.COL3))),2) AS COL3,  
    SUM( CIS2.NVAL(D.COL1))  AS COL4,
    SUM( CIS2.NVAL(D.COL2))  AS COL5, 
    ROUND(SUM( CIS2.NVAL(D.COL2)) / CIS2.NOZERO(SUM(CIS2.NVAL(D.COL1))),2) AS COL6, 
    SUM(CIS2.NVAL(D.COL3))  AS COL7,
    SUM(CIS2.NVAL(D.COL4))  AS COL8, 
    ROUND(SUM(CIS2.NVAL(D.COL4)) / CIS2.NOZERO(SUM(CIS2.NVAL(D.COL3))),2) AS COL9, 
    SUM( CIS2.NVAL(D.COL5) )  AS COL10, 
    SUM( ROUND(CIS2.NVAL(D.COL6),2)   )  AS COL11 
FROM 
  CIS2.VW_DATA_ALL D
  INNER JOIN CIS2.RENIM RE ON (D.CUIIO=RE.CUIIO AND D.CUIIO_VERS=RE.CUIIO_VERS)
WHERE
  D.PERIOADA IN (:pPERIOADA) AND 
  D.FORM_VERS = :pFORM_VERS     AND    
  (:pID_MDTABLE=:pID_MDTABLE) AND
  D.CUATM_FULL LIKE '%'||:pCOD_CUATM||';%' AND
  D.FORM IN (53)                 AND 
  D.CAPITOL IN (1074) AND
  D.RIND IN ('180') 
GROUP BY
  RE.CUIIO,
  RE.DENUMIRE
  
  UNION
SELECT 
  0 AS ORDINE,
  'TOTAL' AS NUME_ROW,
  SUM(CIS2.NVAL(D.COL1) + CIS2.NVAL(D.COL3))  AS COL1,
    SUM(CIS2.NVAL(D.COL4) + CIS2.NVAL(D.COL2) )  AS COL2,
    ROUND(SUM(CIS2.NVAL(D.COL4) + CIS2.NVAL(D.COL2)) / CIS2.NOZERO(SUM(CIS2.NVAL(D.COL1) + CIS2.NVAL(D.COL3))),2) AS COL3,  
    SUM( CIS2.NVAL(D.COL1))  AS COL4,
    SUM( CIS2.NVAL(D.COL2))  AS COL5, 
    ROUND(SUM( CIS2.NVAL(D.COL2)) / CIS2.NOZERO(SUM(CIS2.NVAL(D.COL1))),2) AS COL6, 
    SUM(CIS2.NVAL(D.COL3))  AS COL7,
    SUM(CIS2.NVAL(D.COL4))  AS COL8, 
    ROUND(SUM(CIS2.NVAL(D.COL4)) / CIS2.NOZERO(SUM(CIS2.NVAL(D.COL3))),2) AS COL9, 
    SUM( CIS2.NVAL(D.COL5) )  AS COL10, 
    SUM( ROUND(CIS2.NVAL(D.COL6),2)   )  AS COL11 
FROM 
  CIS2.VW_DATA_ALL D
  INNER JOIN CIS2.RENIM RE ON (D.CUIIO=RE.CUIIO AND D.CUIIO_VERS=RE.CUIIO_VERS)
WHERE
  D.PERIOADA IN (:pPERIOADA) AND 
  D.FORM_VERS = :pFORM_VERS     AND    
  (:pID_MDTABLE=:pID_MDTABLE) AND
  D.CUATM_FULL LIKE '%'||:pCOD_CUATM||';%' AND
 D.FORM IN (53)                 AND 
  D.CAPITOL IN (1074) AND
  D.RIND IN ('180') 
  )
 


;
   END IF;
  END LOOP;
END; -- =======================================================================