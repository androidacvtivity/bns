SELECT 
  L.FORM,
  L.FORM_VERS,
  L.PERIOADA,
  1207 ID_USER,
  SYSDATE DATA_REG,
  L.STATUT        

FROM
(
SELECT
D.*
FROM CIS.DATA_STATUS D
WHERE       
 D.ID_USER = 278
 AND D.FORM = 5
 
 ORDER BY 
 D.PERIOADA 
 
 
 ) L LEFT JOIN (
SELECT
D.*
FROM CIS.DATA_STATUS D
WHERE       
 D.ID_USER = 1207
 AND D.FORM = 5
 
 ORDER BY 
 D.PERIOADA 
 
 
 ) R ON R.PERIOADA = L.PERIOADA 
 
 WHERE 
 R.PERIOADA IS NULL