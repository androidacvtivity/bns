    NVAL(SUM(CASE WHEN D.RIND='01'  AND D.PERIOADA = :pPERIOADA AND VC.FULL_CODE LIKE '%'||CD.CODE||';%' THEN  D.COL2 END)) AS COL1, 
    NVAL(SUM(CASE WHEN D.RIND='03'  AND D.PERIOADA = :pPERIOADA AND VC.FULL_CODE LIKE '%'||CD.CODE||';%' THEN  D.COL2 END)) AS COL2,
    NVAL(SUM(CASE WHEN D.RIND='04'  AND D.PERIOADA = :pPERIOADA AND VC.FULL_CODE LIKE '%'||CD.CODE||';%' THEN  D.COL2 END)) AS COL3,  
    NVAL(SUM(CASE WHEN D.RIND='05'  AND D.PERIOADA = :pPERIOADA AND VC.FULL_CODE LIKE '%'||CD.CODE||';%' THEN  D.COL2 END)) AS COL4,
    NVAL(SUM(CASE WHEN D.RIND='06'  AND D.PERIOADA = :pPERIOADA AND VC.FULL_CODE LIKE '%'||CD.CODE||';%' THEN  D.COL2 END)) AS COL5,
   ROUND((NVAL(SUM(CASE WHEN D.RIND='01'  AND D.PERIOADA = :pPERIOADA AND VC.FULL_CODE LIKE '%'||CD.CODE||';%' THEN  D.COL2 END))/
    NOZERO( NVAL(SUM(CASE WHEN D.RIND='01'  AND D.PERIOADA = :pPERIOADA-4 AND VC.FULL_CODE LIKE '%'||CD.CODE||';%' THEN  D.COL2 END)))*100),1) AS COL6, 
    NVAL(SUM(CASE WHEN D.RIND='01'  AND D.PERIOADA = :pPERIOADA AND VC.FULL_CODE LIKE '%'||CD.CODE||';%' THEN  D.COL3 END)) AS COL7,
    NVAL(SUM(CASE WHEN D.RIND='03'  AND D.PERIOADA = :pPERIOADA AND VC.FULL_CODE LIKE '%'||CD.CODE||';%' THEN  D.COL3 END)) AS COL8,
    NVAL(SUM(CASE WHEN D.RIND='04'  AND D.PERIOADA = :pPERIOADA AND VC.FULL_CODE LIKE '%'||CD.CODE||';%' THEN  D.COL3 END)) AS COL9,
    NVAL(SUM(CASE WHEN D.RIND='05'  AND D.PERIOADA = :pPERIOADA AND VC.FULL_CODE LIKE '%'||CD.CODE||';%' THEN  D.COL3 END)) AS COL10,
    NVAL(SUM(CASE WHEN D.RIND='06'  AND D.PERIOADA = :pPERIOADA AND VC.FULL_CODE LIKE '%'||CD.CODE||';%' THEN  D.COL3 END)) AS COL11
   
  
  FROM
              VW_DATA_ALL D
                INNER JOIN RENIM R ON (R.CUIIO= D.CUIIO AND R.CUIIO_VERS=D.CUIIO_VERS)
                INNER JOIN VW_CL_CUATM VC ON (VC.CODUL = R.CUATM)
                
                CROSS JOIN 

(
 SELECT
      
      DECODE(CS.CODUL, 
      '0000000','01','0100000','02','1111111','03','0300000','04',
      '1400000','05','3400000','06','3600000','07','4100000','08','4300000','09',
      '4500000','10','4800000','11','6200000','12','7100000','13','7400000','14',
      '7800000','15','2222222','16','1000000','17','2500000','18','3100000','19',
      '3800000','20','5300000','21','5500000','22','6000000','23','6400000','24',
      '6700000','25','8000000','26','8300000','27','8900000','28','9200000','29',
      '3333333','30','1200000','31','1700000','32','2100000','33','2700000','34',
      '2900000','35','5700000','36','8500000','37','8700000','38','9600000','39')
      AS NR_ROW,
      CS.DENUMIRE AS DEN_CUATM,
      CS.CODUL AS CODE

    FROM
      VW_CL_CUATM CS

    WHERE
      CS.CODUL 
      IN(
         '0000000','0100000','1111111','0300000','1400000','3400000','3600000','4100000','4300000','4500000',
         '4800000','6200000','7100000','7400000','7800000','2222222','1000000','2500000','3100000','3800000',
         '5300000','5500000','6000000','6400000','6700000','8000000','8300000','8900000','9200000','3333333',
         '1200000','1700000','2100000','2700000','2900000','5700000','8500000','8700000','9600000'
        )
  
) CD

WHERE  D.PERIOADA IN (:pPERIOADA,:pPERIOADA-4) AND
       D.FORM = :pFORM AND
       D.FORM_VERS = :pFORM_VERS 
      -- AND  VC.PRGS  IN ('2')
       --R.TIP_LOCAL ='1'
GROUP BY  CD.CODE , CD.DEN_CUATM

