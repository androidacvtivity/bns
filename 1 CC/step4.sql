SELECT 
  A.CUIIO,
  A.CUIIO_VERS,
  A.DENUMIRE,
  A.CUATM,
  A.CFOJ,
  A.CFP,
  A.CAEM2   
FROM USER_BANCU.KAT_1_CC A
--2995
  LEFT JOIN CIS2.VW_CL_CUATM C ON (C.CODUL = A.CUATM)
  LEFT JOIN CIS2.VW_CL_CAEM2 CA ON  (CA.CODUL = A.CAEM2)
  LEFT JOIN CIS2.VW_CL_CFOJ CF ON  (CF.CODUL = A.CFOJ)
  LEFT JOIN CIS2.VW_CL_CFP CFP ON  (CFP.CODUL = A.CFP)
  
  WHERE 
  C.CODUL IS NOT    NULL 
  
  OR  
  
  CA.CODUL IS  NOT   NULL 
  
  OR  
  
   CF.CODUL IS   NOT   NULL 


  OR  
  
   CFP.CODUL IS   NOT   NULL 