SELECT 
DISTINCT D.CUIIO,
D.CAPITOL,
D.RIND,
D.COL1,
D.COL2,
D.COL3,
D.COL4,
D.COL5,
D.COL6

FROM CIS2.VW_DATA_ALL D

WHERE 



D.RIND IN ('01-F','01-T')
AND D.FORM = 3
AND D.PERIOADA = 2009
--AND D.CUIIO = 40857325
GROUP BY 
D.CUIIO,
D.CAPITOL,
D.RIND,
D.COL1,
D.COL2,
D.COL3,
D.COL4,
D.COL5,
D.COL6


HAVING 
D.COL1 <> 0

ORDER BY 
D.CUIIO