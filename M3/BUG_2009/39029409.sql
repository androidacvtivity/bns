SELECT 
DISTINCT D.CUIIO,
D.CAPITOL,
D.RIND,
D.COL1

FROM USER_EREPORTING.VW_DATA_ALL_PRIMIT D

WHERE 



D.RIND IN ('01-F','01-T')
AND D.FORM = 3
AND D.PERIOADA = 2009

GROUP BY 
D.CUIIO,
D.CAPITOL,
D.RIND,
D.COL1


HAVING 
D.COL1 <> 0



UNION ALL 


SELECT *
FROM CIS2.VW_DATA_ALL

WHERE 

CUIIO IN (
39029409,
39029409,
40569551
)

AND FORM = 3
AND PERIOADA = 2009