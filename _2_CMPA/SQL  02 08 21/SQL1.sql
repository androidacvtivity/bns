SELECT
1 ORDINE,
'Pasari - total(060,070,080,090)'  RIND,
COUNT (DISTINCT CASE WHEN D.PERIOADA = 1040 AND D.RIND IN ('060','070','080','090') AND NVAL(D.COL1) <> 0  THEN D.UNIT_CODE ELSE NULL END ) AS COL1,
COUNT (DISTINCT CASE WHEN D.PERIOADA = 1043 AND D.RIND IN ('060','070','080','090') AND NVAL(D.COL16) <> 0  THEN D.UNIT_CODE ELSE NULL END ) AS COL2,
COUNT (DISTINCT CASE WHEN D.PERIOADA = 1045 AND D.RIND IN ('060','070','080','090') AND NVAL(D.COL1) <> 0  THEN D.UNIT_CODE ELSE NULL END ) AS COL3,
COUNT (DISTINCT CASE WHEN D.PERIOADA = 1047 AND D.RIND IN ('060','070','080','090') AND NVAL(D.COL16) <> 0  THEN D.UNIT_CODE ELSE NULL END ) AS COL4,
COUNT (DISTINCT CASE WHEN D.PERIOADA = 1048 AND D.RIND IN ('060','070','080','090') AND NVAL(D.COL1) <> 0  THEN D.UNIT_CODE ELSE NULL END ) AS COL5
      
     
FROM
  VW_DATA_ALL_GC  D
WHERE
  (D.PERIOADA IN  (1048,1049,1047,1050,1051,1046,1045,1044,1043,1042,1041,1040))  AND   
  (D.FORM =:pFORM) AND
  
  D.FORM IN (58)  AND
  D.CAPITOL IN (416)
  AND D.RIND IN ('060','070','080','090')
  