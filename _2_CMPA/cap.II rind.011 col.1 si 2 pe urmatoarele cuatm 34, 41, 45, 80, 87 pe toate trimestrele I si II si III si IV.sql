


SELECT
D.PERIOADA, 
D.UNIT_CODE,
D.CUATM,
D.RIND,
D.COL1,
D.COL2,
D.COL3,
D.COL4,
D.COL5

FROM
  VW_DATA_ALL_GC  D

WHERE
  (D.PERIOADA IN (1040,1041,1042,1043))  AND   
  (D.FORM =:pFORM) AND
  (D.FORM_VERS =:pFORM_VERS) AND 
  (:pID_MDTABLE =:pID_MDTABLE) AND 
  (D.CUATM_FULL LIKE '%'||:pCOD_CUATM||';%') AND
  D.FORM IN (58)  AND
  D.CAPITOL IN (417)
 

ORDER BY 
 D.PERIOADA, 
D.UNIT_CODE,
D.CUATM,
D.RIND
   