         NULL AS COL10,
         
         
         CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN ('1112') AND D.RIND IN (RR.RIND)  AND D.CUATM_FULL LIKE '%'||:pCOD_CUATM||';%' THEN D.COL2 ELSE NULL END)) AS COL11,
         
