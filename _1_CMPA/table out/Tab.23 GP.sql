         --CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN ('1112') AND D.RIND IN (RR.RIND)  AND ((D.CUATM_FULL LIKE '%'||'2222222'||';%') OR (D.CUATM_FULL LIKE '%0100000;%')) THEN D.COL1 ELSE NULL END)) AS COL4,
         NULL AS COL4,
         
         CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN ('1112') AND D.RIND IN (RR.RIND)  AND ((D.CUATM_FULL LIKE '%'||'2222222'||';%') OR (D.CUATM_FULL LIKE '%0100000;%')) THEN D.COL2 ELSE NULL END)) AS COL5,
         
         CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN ('1112') AND D.RIND IN (RR.RIND)  AND ((D.CUATM_FULL LIKE '%'||'2222222'||';%') OR (D.CUATM_FULL LIKE '%0100000;%')) THEN D.COL2 ELSE NULL END))/
         NOZERO(CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN ('1111') AND D.RIND IN ('040')  AND ((D.CUATM_FULL LIKE '%'||'2222222'||';%') OR (D.CUATM_FULL LIKE '%0100000;%')) THEN D.COL1 ELSE NULL END)))*100 AS COL6,
         
         
                  
