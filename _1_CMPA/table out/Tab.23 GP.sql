         NULL AS COL4,
         
         CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN ('1112') AND D.RIND IN (RR.RIND)  AND ((D.CUATM_FULL LIKE '%'||'3333333'||';%') OR (D.CUATM_FULL LIKE '%'||9600000||';%')) THEN D.COL2 ELSE NULL END)) AS COL8,
         
         CIS2.NVAL(SUM(CASE WHEN D.CAPITOL IN ('1112') AND D.RIND IN (RR.RIND)  AND ((D.CUATM_FULL LIKE '%'||'3333333'||';%') OR (D.CUATM_FULL LIKE '%'||9600000||';%')) THEN D.COL2 ELSE NULL END))/
