
SELECT 
L.CAPITOL ER_CAPITOL,
R.CAPITOL CIS2_CAPITOL,
L.CAPITOL_VERS ER_CAPITOL_VERS,
R.CAPITOL_VERS CIS2_CAPITOL_VERS,
L.RIND ER_RIND,
R.RIND CIS2_RIND,
L.ID_MD ER_ID_MD,
R.ID_MD CIS2_ID_MD,
L.RIND_VERS ER_RIND_VERS,
R.RIND_VERS CIS2_RIND_VERS

FROM 


(
SELECT 
CAPITOL,
CAPITOL_VERS,
RIND,
ID_MD,
RIND_VERS

FROM USER_EREPORTING.VW_DATA_ALL_FR_PRIMIT

WHERE



FORM = 63 
AND PERIOADA = 2009

AND CUIIO = 40649271

AND CAPITOL = 1120 ) L RIGHT JOIN (

SELECT 
CAPITOL,
CAPITOL_VERS,
RIND,
ID_MD,
RIND_VERS

FROM CIS2.MD_RIND

WHERE
CAPITOL = 1120
 AND CAPITOL_VERS IN (2009)
 AND FORM = 63


) R ON R.ID_MD = L.ID_MD 


WHERE 
L.ID_MD IS NOT NULL 

ORDER BY 
L.RIND