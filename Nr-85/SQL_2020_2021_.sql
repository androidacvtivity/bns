SELECT
D.CUIIO, 
R.DENUMIRE,
R.CUATM,
R.CAEM2,
SUM(CASE WHEN D.PERIOADA IN (2009) AND  D.CAPITOL IN (1001) AND D.RIND IN ('010') THEN NVAL(D.COL1) ELSE 0 END ) AS CAP_I_rind010_col1_2020,
SUM(CASE WHEN D.PERIOADA IN (2010) AND  D.CAPITOL IN (1001) AND D.RIND IN ('010') THEN NVAL(D.COL1) ELSE 0 END ) AS CAP_I_rind010_col1_2021,

SUM(CASE WHEN D.PERIOADA IN (2009) AND  D.CAPITOL IN (1001) AND D.RIND IN ('010') THEN NVAL(D.COL2) ELSE 0 END ) AS CAP_I_rind010_col2_2020,
SUM(CASE WHEN D.PERIOADA IN (2010) AND  D.CAPITOL IN (1001) AND D.RIND IN ('010') THEN NVAL(D.COL2) ELSE 0 END ) AS CAP_I_rind010_col2_2021,

SUM(CASE WHEN D.PERIOADA IN (2009) AND  D.CAPITOL IN (1002) AND D.RIND IN ('010') THEN NVAL(D.COL1) ELSE 0 END ) AS CAP_II_rind010_col1_2020,
SUM(CASE WHEN D.PERIOADA IN (2010) AND  D.CAPITOL IN (1002) AND D.RIND IN ('010') THEN NVAL(D.COL1) ELSE 0 END ) AS CAP_II_rind010_col1_2021,

SUM(CASE WHEN D.PERIOADA IN (2009) AND  D.CAPITOL IN (1004) AND D.RIND IN ('010') THEN NVAL(D.COL1) ELSE 0 END ) AS CAP_IV_rind010_col1_2020,
SUM(CASE WHEN D.PERIOADA IN (2010) AND  D.CAPITOL IN (1004) AND D.RIND IN ('010') THEN NVAL(D.COL1) ELSE 0 END ) AS CAP_IV_rind010_col1_2021,

SUM(CASE WHEN D.PERIOADA IN (2009) AND  D.CAPITOL IN (1007) AND D.RIND IN ('010') THEN NVAL(D.COL1) ELSE 0 END ) AS CAP_VII_rind010_col1_2020,
SUM(CASE WHEN D.PERIOADA IN (2010) AND  D.CAPITOL IN (1007) AND D.RIND IN ('010') THEN NVAL(D.COL1) ELSE 0 END ) AS CAP_VII_rind010_col1_2021,

SUM(CASE WHEN D.PERIOADA IN (2009) AND  D.CAPITOL IN (1007) AND D.RIND IN ('010') THEN NVAL(D.COL2) ELSE 0 END ) AS CAP_VII_rind010_col2_2020,
SUM(CASE WHEN D.PERIOADA IN (2010) AND  D.CAPITOL IN (1007) AND D.RIND IN ('010') THEN NVAL(D.COL2) ELSE 0 END ) AS CAP_VII_rind010_col2_2021,

SUM(CASE WHEN D.PERIOADA IN (2009) AND  D.CAPITOL IN (1007) AND D.RIND IN ('130') THEN NVAL(D.COL1) ELSE 0 END ) AS Informativ_rind_130_2020,
SUM(CASE WHEN D.PERIOADA IN (2010) AND  D.CAPITOL IN (1007) AND D.RIND IN ('130') THEN NVAL(D.COL1) ELSE 0 END ) AS Informativ_rind_130_2021
    FROM CIS2.VW_DATA_ALL  D
                INNER JOIN CIS2.RENIM R ON R.CUIIO = D.CUIIO AND R.CUIIO_VERS = D.CUIIO_VERS
    
        WHERE
 
        D.PERIOADA IN (2009,2010)
        AND D.FORM  IN (17)
        

       GROUP BY 
     D.CUIIO, 
R.DENUMIRE,
R.CUATM,
R.CAEM2

ORDER BY 
R.CUATM,
D.CUIIO