SELECT
:pPERIOADA AS PERIOADA,
:pFORM AS FORM,
:pFORM_VERS AS FORM_VERS,
:pID_MDTABLE AS ID_MDTABLE,
:pCOD_CUATM AS COD_CUATM, 
 NTL.CODUL AS NR_SECTIE,
 NTL.DENUMIRE AS NUME_SECTIE,
 
'0' AS NR_SECTIE1,
'0' AS NUME_SECTIE1,
'0' AS NR_SECTIE2,
'0' AS NUME_SECTIE2,
 MR.RIND||'~'||NTL.CODUL AS NR_ROW,
 MR.RIND AS ORDINE,
'00' AS DECIMAL_POS, 
 MR.DENUMIRE AS NUME_ROW,
 SUM(CASE WHEN MR.RIND  = D.RIND THEN  D.COL1 ELSE NULL END)  AS COL1,
 SUM(CASE WHEN MR.RIND  = D.RIND THEN  D.COL2 ELSE NULL END)  AS COL2

 


  
FROM CIS2.VW_DATA_ALL D

CROSS JOIN 
(
        SELECT 
             MR.RIND,
             MR.DENUMIRE,
             MR.RIND_VERS
            
            FROM CIS2.MD_RIND MR
            
            WHERE
            MR.CAPITOL IN (1007)
            AND MR.RIND_VERS IN (2007)
            
            AND 
            ASCII(MR.RIND) <> 45 
            
) MR
 
 INNER JOIN CIS2.RENIM RN ON (RN.CUIIO=D.CUIIO AND RN.CUIIO_VERS=D.CUIIO_VERS)
 INNER JOIN CIS2.CL_N85_NTL NTL ON (NTL.CODUL=RN.N85_NTL)

        

WHERE
(D.PERIOADA =:pPERIOADA) AND
(D.FORM =:pFORM) AND 
(D.FORM_VERS =:pFORM_VERS) AND
(:pID_MDTABLE=:pID_MDTABLE) AND 
(D.CUATM_FULL LIKE '%' ||:pCOD_CUATM||';%') AND
D.FORM = 17   
AND D.CAPITOL IN (1007)



    GROUP BY 
    MR.RIND, 
    MR.DENUMIRE,
    MR.RIND_VERS,
    NTL.CODUL,
    NTL.DENUMIRE


     
    ORDER BY
    
    MR.RIND 